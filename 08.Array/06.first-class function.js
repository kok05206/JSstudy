/**
 * 일급함수
 * 정의 : 함수가 일반 객체처럼 모드 연산이 가능한 것.
 * 1. 함수의 매개변수로 전달.
 * 2. 함수의 반환값.
 * 3. 할당 명령문.
 * 4. 동일 비교 대상/
 *
 * 고차함수(Higher-order function)
 * 인자로 함수를 받는 것 (콜백함수 - Call-Back function)
 * 함수를 반환하는 함수를 고차함수
 *
 * 순수함수
 * 정의 : 함수안에서 불변성을 유지하는 함수
 * 불변성 : 함수안에서 전달받은 매개변수나 또는 함수 밖에있는 특정한 상태를 함수내부에서 수정하지 않고, 전달받은 인자를 통해서 함수에서 특정한 연산이나 일을 수행하고 새로운 값을 리턴
 * 장점 : 에러가 줄고 가독성이 올라간다.
 *
 * 함수형 프로그래밍 특징
 * 1. 데이터를 변경하지 않는다.
 * 2. 변수를 사용하지 않는다.
 * 3. 조건문을 사용하지 않는다.
 * 4. 반복문을 사용하지 않는다.
 */